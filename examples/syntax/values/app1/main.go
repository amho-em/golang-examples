package main

import "fmt"

func main() {
	fmt.Println("String Concatenation:", "go"+"lang")

	fmt.Println("1+1 =", 1+1)

	fmt.Println("7.0/3 =", 7.0/3)
	fmt.Println("7/3 =", 7/3)

	fmt.Println("true && false =", true && false)
	fmt.Println("true || false =", true || false)
	fmt.Println("!true =", !true)
}
